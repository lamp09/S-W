def circle_area(radius):
    area = radius * radius * 3.14
    return area                 # return 에 수식을 넣어서 계산해도 상관없음.

r = int(input('반지름 입력 : '))
area = circle_area(r) # 함수의 area와 다른 이유 - 함수의 area는 계산식을 저장하는 것이고, 메인의 area 는 cicle_area()의 값을 저장하는 변수
print(f'반지름이{r}인 원의 넓이는 {area}')
# 함수 호출하여 돌려받은 결과값을 바로 출력 가능.
print(f'반지름이{r}인 원의 넓이는 {circle_area(r)}')


