# 알고리즘
# * 함수
#   3. 두 정수 전달받기
#       1) 둘 중 큰 수를 판별한다. 만약 첫번째 수가 두번째 수보다 크다면 
#           a. 첫 번째 수 돌려주기
#       2) 아니면 
#           b. 두 번째 수 돌려주기
def max_num(num1,num2):
    if num1 > num2 :
        return num1
    else :
        return num2 
    
# * 메인 
#   1. 두 정수 입력 받기
#   2. 두 정수를 가지고 함수 호출하기
#   4. 돌려받은 결과 출력하기.

num1 = int(input('첫 번째 수 입력 :'))
num2 = int(input('두 번째 수 입력 :'))


print('두 숫자 중 큰수 :', max_num(num1,num2) )



